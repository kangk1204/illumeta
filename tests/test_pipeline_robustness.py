BRANCH = "minfi"
DMP = "Minfi_DMPs_full.csv"


def _write_csv(path: Path, header: list[str], rows: list[list[object]]) -> None:
    with path.open("w", encoding="utf-8", newline="") as handle:
        writer = csv.writer(handle)
        writer.writerow(header)
        writer.writerows(rows)


def _cohort(tmp_path: Path, header: list[str], rows: list[list[object]]) -> MetaCohort:
    d = tmp_path
    _write_csv(d / DMP, header, rows)
    return MetaCohort(cohort_id="GSEx", result_dir=d)


