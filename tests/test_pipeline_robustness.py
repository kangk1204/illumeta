def test_s3_A9_tiny_se_underflow_no_crash():
    """A positive-but-extreme SE underflows se*se to 0.0; the `se>0` guard must
    not let 1/(se*se) raise ZeroDivisionError."""
    res = _random_effect_meta_one([0.5, 0.5, 0.5], [1e-200, 0.1, 0.1], [True, True, True])
    assert res["k"] == 3
    assert isinstance(res["random_effect"], float)  # finite or NaN, never a crash


def test_s3_A9_tiny_se_yields_zero_weight():
    """The guarded inverse-variance weight is 0 for an underflowing SE and
    unchanged (1/se^2) for a normal SE."""
    assert _inv_var_weight(1e-200, True) == 0.0
    assert _inv_var_weight(0.0, True) == 0.0
    assert _inv_var_weight(float("nan"), True) == 0.0
    assert math.isclose(_inv_var_weight(0.1, True), 100.0)
    assert math.isclose(_inv_var_weight(0.1, True, extra=0.03), 1.0 / 0.04)


def test_s3_A9_degenerate_se_excluded_from_effective_count(tmp_path):
    """Semantic guard: a degenerate-SE cohort must NOT count toward the effective
    cohort total (n_valid_cohorts / min-cohorts gate), only crash-avoidance."""
    cohorts = []
    for i, se in enumerate(["1e-200", "0.10", "0.10"]):  # cohort 0 is degenerate
        d = tmp_path / f"c{i}"
        d.mkdir()
        _write_csv(d / DMP, ["CpG", "logFC", "SE", "P.Value"], [["cg1", "0.5", se, "0.01"]])
        cohorts.append(MetaCohort(cohort_id=f"GSE{i}", result_dir=d, n_con=10, n_test=10))
    ids = _cohort_column_ids(cohorts)
    rows, _summary, _w = _analyze_branch(
        cohorts, ids, BRANCH, DMP,
        MetaThresholds(min_cohorts=2, partial_conjunction_r=2),
        allow_missing_branches=False,
    )
    row = next(r for r in rows if r["CpG"] == "cg1")
    assert row["n_valid_cohorts"] == 2  # degenerate cohort excluded, not 3


